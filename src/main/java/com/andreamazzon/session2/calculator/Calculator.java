package com.andreamazzon.session2.calculator;

/**
 * Very simple example of static method: here you can note that the method does not depend on fields
 * of the class (the class actually has no fields).
 *
 * @author Andrea Mazzon
 *
 */
public class Calculator {

	static double computeRatio(int a, int b){
		return (double) a/b;
	}
}
